package vladislavs.uhovs.lesson7.selfcheck.returning;

public class Calculation {

    public int sum(int a, int b){
        return a + b;
    }
    public double div(int a, int b){
        return (double) a / b;
    }
    public int sub(int a, int b){
        return a - b;
    }
    public int mul(int a, int b){
        return a * b;
    }

}
