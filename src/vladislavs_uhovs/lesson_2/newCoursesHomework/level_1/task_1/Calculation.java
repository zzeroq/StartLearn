package vladislavs_uhovs.lesson_2.newCoursesHomework.level_1.task_1;

public class Calculation {

    public static void main(String[] args) {


        int number1 = 5;
        int number2 = 15;
        int sum = number2 + number1;
        System.out.println(number1 + " + " + number2 + " = " + sum);
        int divide = number2 / number1;
        System.out.println(number1 + " / " + number2 + " = " + divide);
        int multiply = number1 * number2;
        System.out.println(number1 + " * " + number2 + " = " + multiply);
        int substruct = number2 - number1;
        System.out.println(number1 + " - " + number2 + " = " + substruct);






    }



}
