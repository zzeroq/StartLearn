package vladislavs_uhovs.lesson1;

class Calculations {
    public static void main(String[] args) {

        double number1 = 11.0;
        double number2 = 33.0;
        double number3 = 53.0;

        double Multiply = number1 * number2 / number3;
        System.out.println("Multiply = " + Multiply);

        double Subs = number1 - number2;
        System.out.println("Subs = " + Subs);

        double Sum = number1 + number2;
        System.out.println("Sum = " + Sum);

        double Divide = number1 / number2;
        System.out.println("Divide = " + Divide);

        int number1_0 = 25;
        int number2_0 = 22;
        int SumInt = number1_0 + number2_0;
        System.out.println("SumInt = " + SumInt);

        double numberMax = Math.pow(2, 20);
        double SumMax = numberMax + number1_0;
        System.out.println("SumMax = " + SumMax);




    }




}
